// Declaring and Initializing  Pointers in C

// General Syntax:

    // data_type *pointe_name

int x = 5; // Initializing a variable 

int *ptr; // Declaring a pointer here * is called a Dereference Operator / Value of Operator

ptr = &x; // Assigning the address of variable x inside the pointer

// These 3 lines can also be written as 

int x = 5, *prt = &x;